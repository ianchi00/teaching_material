# 建立變數
a = 1
print("a: ", a)

# 改變變數內容
a = 2
print("a 被改變: ", a)

# 複製變數內容
b = a
print("b 從a複製: ", b)

# 一次設定多個變數的名稱和內容
d, c, e = 4, 5, 6
print("d: ", d)
print("c: ", c)
print("e: ", e)

