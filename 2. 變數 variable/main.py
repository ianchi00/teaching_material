# 資料型態
# int
x = 8
print(type(x))

# float
x = 8.2
print(type(x))

# str
x = "字串"
print(type("字串"))

# bool
x, y = True, False
print(type(x), type(y))

# list
x = [1,2,3]
print(type(x))

# dict
x = {'a':1,'b':2,'c':3}
print(type(x))

# set
x = {1,2,3}
print(type(x))